def compress_history_tags(messages, keep_recent=10, max_len=800, force=False):
    """Compress <thinking>/<tool_use>/<tool_result> in older messages to save tokens.
    GenericAgent insight: this alone cuts token usage 6x."""
    compress_history_tags._cd = getattr(compress_history_tags, '_cd', 0) + 1
    if force: compress_history_tags._cd = 0
    if compress_history_tags._cd % 5 != 0: return messages
    
    _pats = {
        tag: re.compile(rf'(<{tag}>)([\s\S]*?)(</{tag}>)')
        for tag in ('thinking', 'think', 'tool_use', 'tool_result')
    }
    _hist_pat = re.compile(r'<(history|key_info)>[\s\S]*?</\1>')
    
    def _trunc_str(s):
        if isinstance(s, str) and len(s) > max_len:
            return s[:max_len//2] + '\n...[Truncated]...\n' + s[-max_len//2:]
        return s
    
    def _trunc(text):
        text = _hist_pat.sub(lambda m: f'<{m.group(1)}>[...]</{m.group(1)}>', text)
        for pat in _pats.values():
            text = pat.sub(lambda m: m.group(1) + _trunc_str(m.group(2)) + m.group(3), text)
        return text
    
    for i, msg in enumerate(messages):
        if i >= len(messages) - keep_recent: break
        c = msg.get('content', '')
        if isinstance(c, str): msg['content'] = _trunc(c)
        elif isinstance(c, list):
            for b in c:
                if not isinstance(b, dict): continue
                t = b.get('type')
                if t == 'text' and isinstance(b.get('text'), str):
                    b['text'] = _trunc(b['text'])
                elif t == 'tool_result':
                    tc = b.get('content', '')
                    if isinstance(tc, str): b['content'] = _trunc_str(tc)
    
    return messages

def trim_messages_history(history, context_win=128000):
    """Trim history to stay within context window budget.
    Called before every LLM call to avoid overflow."""
    compress_history_tags(history)
    cost = sum(len(json.dumps(m, ensure_ascii=False)) for m in history)
    print(f"[Solomon Context] {cost} chars, {len(history)} messages in history")
    
    if cost > context_win * 3:
        compress_history_tags(history, keep_recent=4, force=True)
        target = context_win * 3 * 0.6
        while len(history) > 5 and cost > target:
            history.pop(0)
            while history and history[0].get('role') != 'user':
                history.pop(0)
            if history and history[0].get('role') == 'user':
                history[0] = dict(history[0])
                history[0]['content'] = str(history[0].get('content', ''))[:500]
    return history

