# Adapted from GenericAgent's skill crystallization concept.
# After every successful task → write a skill file to brain/skills/
# This is the "使用越久越强" (stronger the longer you use it) mechanism.

SKILLS_DIR = "/home/workspace/solomon-vault/brain/skills"

def crystallize_skill(task_name, tool_calls_made, outcome_summary, confidence=0.8):
    """Write a skill file after a successful task.
    Called by the memory system after every successful session."""
    os.makedirs(SKILLS_DIR, exist_ok=True)
    slug = re.sub(r'[^a-z0-9]+', '_', task_name.lower())[:50]
    path = f"{SKILLS_DIR}/{slug}.md"
    
    content = f"""# Skill: {task_name}

> Crystallized from real session on {datetime.now().strftime('%Y-%m-%d')}.
> Confidence: {confidence}

## What this skill does
{outcome_summary}

## Tool chain used
"""
    for tc in tool_calls_made:
        content += f"- `{tc['tool']}({', '.join(tc.get('args', {}).keys())})`\n"
    
    content += f"""
## When to use this skill
Trigger when you see: *{task_name}* or similar task patterns.

---
Auto-generated by Solomon OS GenericAgent integration. Do not edit.
"""
    with open(path, 'w') as f:
        f.write(content)
    print(f"[Solomon Skill] Crystallized: {slug}.md")
    return path

def load_skill(skill_name):
    """Load a crystallized skill file."""
    slug = re.sub(r'[^a-z0-9]+', '_', skill_name.lower())[:50]
    path = f"{SKILLS_DIR}/{slug}.md"
    if os.path.exists(path):
        with open(path) as f: return f.read()
    return None

def list_skills():
    """List all crystallized skills."""
    if not os.path.exists(SKILLS_DIR): return []
    return sorted([f[:-3] for f in os.listdir(SKILLS_DIR) if f.endswith('.md')])